  "          ? 'Invalid year' : '',",
  "    };",
  "    setErrors(e);",
  "    return Object.values(e).every(v => !v);",
  "  };",
  "",
  "  const handleSubmit = async (e) => {",
  "    e.preventDefault();",
  "    if (!validate()) return;",
