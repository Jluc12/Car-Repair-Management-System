  "import { useEffect, useState } from 'react';",
  "import api from '../api/axios';",
  "import toast from 'react-hot-toast';",
  "import { MdAdd, MdDirectionsCar } from 'react-icons/md';",
  "import { FiInfo } from 'react-icons/fi';",
  "",
  "const EMPTY = { plateNumber: '', type: '', model: '', manufacturingYear: '', driverPhone: '', mechanicName: '' };",
  "",
  "// Rwanda plate number regex:",
  "// Standard: RA[A-Z] followed by 3 digits and a consonant  e.g. RAB 123B",
  "// Special prefixes: RDF, RNP, GR, GP, IT followed by 3 digits and a consonant",
  "const CONSONANTS = 'BCDFGHJKLMNPQRSTVWXYZbcdfghjklmnpqrstvwxyz';",
  "const PLATE_RE = /^(RA[A-Z]|RDF|RNP|GR|GP|IT)[\\s-]?\\d{3}[BCDFGHJKLMNPQRSTVWXYZbcdfghjklmnpqrstvwxyz]$/;",
  "const PHONE_RE = /^(078|079|072|073)\\d{7}$/;",
  "const LETTERS_RE = /^[A-Za-z\\s'-]+$/;",
  "",
  "function validatePlate(val) {",
  "  const v = val.trim().toUpperCase().replace(/\\s+/g, ' ');",
  "  if (!v) return 'Plate number is required';",
  "  if (!PLATE_RE.test(v.replace(/\\s/g, ''))) {",
  "    return 'Format: RAA-Z 123C or RDF/RNP/GR/GP/IT 123C (last letter must be a consonant)';",
  "  }",
  "  return '';",
  "}",
  "",
  "function validatePhone(val) {",
  "  const v = val.trim();",
  "  if (!v) return 'Phone number is required';",
  "  if (!/^\\d+$/.test(v)) return 'Phone must contain digits only';",
  "  if (v.length !== 10) return 'Phone must be exactly 10 digits';",
