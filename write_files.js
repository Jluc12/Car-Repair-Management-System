  "      plateNumber:       validatePlate(form.plateNumber),",
  "      driverPhone:       validatePhone(form.driverPhone),",
  "      mechanicName:      validateName(form.mechanicName, 'Mechanic name'),",
  "      type:              form.type.trim()  ? '' : 'Car type is required',",
  "      model:             form.model.trim() ? '' : 'Model is required',",
